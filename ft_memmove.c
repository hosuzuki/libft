#include "libft.h"

void	*ft_memmove(void *dst, const void *src, size_t len)
{
	unsigned char	*d;
	unsigned char	*s;

	d = (unsigned char *)dst;
	s = (unsigned char *)src;
	if (s < d)
	{
		while (len--)
		{
			d[len] = s[len];
			if (len == 0)
				break ;
		}
	}
	else
		ft_memcpy(d, s, len);
	return (dst);
}
