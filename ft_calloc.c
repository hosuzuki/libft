#include "libft.h"

void	*ft_calloc(size_t count, size_t size)
{
	char	*m;

	if (count == 0 || size == 0)
		return (ft_strdup(""));
	m = malloc(count * size);
	if (!m)
		return (NULL);
	ft_bzero(m, count * size);
	return (m);
}
